% Правила:
% 1. Список всех соединении:
list_of_compounds(Compounds) :-
    findall(Compound, organic_compound(Compound), Compounds).

% 2. Проверка на изомерность молекулов:
is_isomeric(X, Y) :-
    molar_mass(X, M_mol1),
    molar_mass(Y, M_mol2),
    X\=Y,
    M_mol1=M_mol2.
is_isomeric(Y, X) :-
    molar_mass(Y, M_mol1),
    molar_mass(X, M_mol2),
    Y\=X,
    M_mol1=M_mol2.

% 3. Проверка на лёгкость молекулов:
is_lightweight(X) :-
    molar_mass(X, M_mol), M_mol=<90.

% 4. Проверка на плотность веществ по сравнению с водой (1000кг/м^3):
is_dense(X) :-
    density(X, Rho), Rho > 1000.

% 5. Проверка на агрегатное состояние вещества при комнатной температуре (25°C).
state_of_matter(X) :-
    temperatures(X, T_1, T_2),
    ( T_1 < 25, T_2 > 25
    -> State = 'жидким'
    ; T_1 > 25
    -> State = 'твёрдым'
    ; T_2 < 25
    -> State = 'газообразным'
    ),
    format('Вещество является ~w.', [State]).

% 6. Проверка на летучесть вещества (сравнивается с водой при 20°C).
is_volatile(X) :-
    vapor_pressure(X, P),
    ( number(P)
    -> P > 2.3388
    ; P = 'Empty'
    ).

% 7. Проверка на растворимости в воде:
% - <0,1: нерастворимый;
% - 0,1-1: малорастворимый;
% - >1: растворимый;
% - 'miscible': неограниченно.
solubility(X) :-
    water_solubility(X, G),
    ( number(G)
    ->  (   G<0.1
        ->  Sol = 'нерастворимым'
        ; G>=0.1; G<1
        ->  Sol = 'малорастворимым'
        ; G>=1
        ->  Sol = 'растворимым'
        )
    ;  Sol = 'неограниченно растворимым'
    ),
    format('Вещество является ~w в воде.', [Sol]).

% 8. Проверка безопасности вещества по токсичности:
% 0 — безопасный;
% 1 — вызывает раздражение;
% 2 — вред при интенсивном и/или длительном воздействий;
% 3 — вред при кратковременном воздействий;
% 4 — смерть при очень кратковременном воздействий.
% Проверка безопасности вещества по огнеопасности:
% 0 — негорючее;
% 1 — трудногорючее;
% 2 — отн. высокая температура воздуха;
% 3 — при температуре воздуха;
% 4 — ниже температуры воздуха.
% Проверка безопасности вещества по стабильности:
% 0 — стабильный;
% 1 — обычно стабильный, но может стать неустойчивым при повышенных температуре и давлении;
% 2 — неустойчивый при повышенных температуре и давлении;
% 3 — может детонировать при специфичных условиях;
% 4 — может детонировать.
nfpa_hazard(X, H, F, R) :-
    nfpa(X, H, F, R, _).

% 9. Проверка безопасности вещества по специальной критерии:
% SA — удушающий газ;
% COR — коррозивное вещество;
% ACID — кислота;
nfpa_special_hazard(X) :-
    nfpa(X, _, _, _, S),
    ( S = 'SA'
    -> Factor = 'удушающим газом'
    ; S = 'COR'
    -> Factor = 'коррозивным'
    ; S = 'ACID'
    -> Factor = 'кислотой'
    ; S = 'Empty'
    -> writeln('Вещество не имеет особенной характеристики.'),
    fail
    ),
    format('Вещество является ~w (~w).', [Factor, S]).

% 10. Средний балл безопасности по NFPA:
nfpa_avg(X, Avg) :-
    nfpa(X, H, F, R, _),
    Avg is (H+F+R)/3.

% 11. Список веществ со специальными обозначениями:
list_of_specials(X_list, S) :-
    findall(X, nfpa(X, _, _, _, S), X_list).

% 12. Сравнение безопастности веществ по их средним значеним по NFPA.
nfpa_compare(X, Y) :-
    nfpa_avg(X, Avg_1), nfpa_avg(Y, Avg_2),
    (   Avg_1 > Avg_2
    -> format('~w опаснее, чем ~w.', [X, Y])
    ;  Avg_1 < Avg_2
    -> format('~w опаснее, чем ~w.', [Y, X])
    ;  format('~w эквивалентен ~w.', [X, Y])
    ).
% 13. Список летучих веществ.
list_of_volatiles(Vol) :-
    findall(V, is_volatile(V), Vol).

% 14. Принадлежность соединения к химическому классу.
belongs_to_class(X, Y) :-
    chemical_class(X, Y).